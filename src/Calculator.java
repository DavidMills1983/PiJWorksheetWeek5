public class Calculator {
    double add (int x, int y){
        return (double) x + y;
    }

    double subtract(int x, int y){
        return (double) x - y;
    }

    double multiply(int x, int y){
        return (double) x * y;
    }

    double divide(int x, int y){
        return (double) x / (double) y;
    }

    double modulus(int x, int y){
        return (double) x % (double) y;
    }
}
